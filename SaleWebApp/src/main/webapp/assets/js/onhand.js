function forUpdate(name,price,count,unit,id){
    var nameUp = document.getElementById("nameUp");
    nameUp.value=name;
    var priceUp = document.getElementById("priceUp");
    priceUp.value = price;
    var countUp = document.getElementById("countUp");
    countUp.value = count;
    var unitUp = document.getElementById("unitUp");
    unitUp.value = unit;
    var idUp = document.getElementById("idUp");
    idUp.value = id;
}


